const clearActive = () => {
  pages.forEach((page) => {
    if (page.classList.contains("active")) {
      page.classList.remove("active");
    }
  });
};

pages.forEach((page) => {
  page.children[0].addEventListener("click", (e) => {
    e.preventDefault();
    if (!page.classList.contains("active")) {
      clearActive();
      page.classList.add("active");
      actVideos();
    }
    window.scrollTo(0, 0);
  });
});
